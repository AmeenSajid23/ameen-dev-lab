# A Set containing different data types and does not allow duplicates
# Note: Sets are unordered collections, so the elements may appear in a different order each time you print the set.
#############################################################################################

my_set = {1, "Hello", 3, 4.35, 5, True, 25, 5, 5}  # Create a set with unique integers and no duplicates
print(my_set)  # Output: {1, 3, 4.35, 5, 25, 'Hello', True}


